<h2>Assigning an open source license</h2>

<p>
    When you share your keyboard with the world, you will need to assign a specific
    <a href="https://opensource.org/licenses" target='_blank'>open source license</a>. There are hundreds of prewritten
    licenses, but for keyboards to be included in this repository, you must use the MIT license:
</p>

<dl>
    <dt>MIT license</dt>
    <dd>A permissive license that is short and to the point. It lets people do anything with your code with proper attribution and without warranty.</dd>
</dl>

<p>
    The MIT license is explored in detail at <a href="https://choosealicense.com/licenses/" target='_blank'>choosealicense.com</a>,
    from which the description above was taken.
</p>
