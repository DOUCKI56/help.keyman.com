
Notes on readme:
================

There are two different readmes available for a model in the repository:

•  README.md in the root of the model folder.
• readme.htm in the package file.

These will often overlap but have different target audiences.

README.md is a fairly well understood document visible in GH browsing, targeted at developers.

readme.htm will be shown if the package .kmp is installed manually (as opposed to automatically through the cloud install, which is silent) and introduces the model to the user so they know they are installing the appropriate file.