<h1>Windows Installer XML (WiX)</h1>

<p>WiX is an open source tool that creates Windows Installer .msi files from XML source files. WiX requires .NET framework 1.1, and the
latest version can be downloaded from: <a class="ulink" href="http://wixtoolset.org/" target=
"_blank">http://wixtoolset.org/</a>.</p>

<p>Keyman Developer 9.0 Branding Pack uses WiX version 2.0 to build installers in the Distribution Editor.</p>
