<h1>Introducing Keyman Developer</h1>

<h2><a name="id471208" id="id471208"></a>Welcome to Keyman Developer</h2>

<p>Keyman Developer allows you to create your own keyboard layouts for use on the Keyman platform. Keyman is a multi-platform
input method solution that runs on Windows, macOS, the web, mobile web, iOS and Android. Keyman keyboards can also be read by KMFL
for Linux and UNIX based platforms.</p>

<p>There are two core applications included in Keyman Developer: the Keyman Developer IDE (sometimes called TIKE), and KMComp.</p>

<h2><a name="id471195" id="id471195"></a>Keyman Developer IDE</h2>

<p>The Keyman Developer IDE, or TIKE (Tavultesoft Integrated Keyboard Editor) is a complete environment for designing, developing, testing, and packaging your keyboards for distribution. Some of the major features of Keyman Developer IDE are:</p>

<ul type="disc">
  <li>Keyboard Editor, for creating keyboards visually for all platforms</li>
  <li>Touch Layout editor, for creating fluid touch layouts</li>
  <li>Package Editor, for creating a package of keyboard, fonts and files for distribution on Windows and macOS</li>
  <li>Branding Pack, for creating a Keyman Engine-based product for Windows that includes your keyboards</li>
  <li>Integrated compiler, to compile keyboards and packages</li>
  <li>Fully interactive debugger for testing keyboards</li>
  <li>Character Map, which supports viewing Unicode fonts under all Windows versions</li>
  <li>On Screen Keyboard editor, for creating visual representations of keyboards</li>
  <li>Projects, for managing keyboard development projects large and small</li>
</ul>

<h2><a name="id471278" id="id471278"></a>KMComp</h2>

<p>KMComp, the command-line compiler, is a straightforward tool that lets you compile keyboards and packages from the command-line.</p>
