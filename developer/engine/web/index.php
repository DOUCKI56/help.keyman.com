<h1>Keyman Engine for Web Help</h1>
<p>
  The Keyman Engine for Web may be used for all your typing needs for your new or existing web application or website. 
  With the Keyman Engine for Web, you can access the Keyman Cloud Services library of hundreds of keyboards supporting more than a thousand languages, 
  or build your own keyboard layouts using Keyman Developer.
</p>
<p>
  You can choose to integrate the Keyman Engine for Web into your site either with a Keyman Engine for Web subscription or by hosting the engine from 
  your own site with the Professional Edition.  The following links provide information about obtaining, configuring and using either edition.
</p>

