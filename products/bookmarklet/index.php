<h2 class="center">Keyman Bookmarklet</h2>
<h3>How to install the bookmarklet</h3>
<ol>
    <li>Visit <a href="http://keymanweb.com">keymanweb.com</a></li>
    <li>Select the language you want to type in from the top menu</li>
    <li>Click and drag the bookmarklet into your browsers Bookmarks toolbar</li>
</ol>
<br/>
<h3>Use the bookmarklet on any website</h3>
<ol>
    <li>Load any web page, such as a search engine.</li>
    <li>Click the KeymanWeb Keyboard bookmarklet in your Bookmarks toolbar or menu.</li>
    <li>You may need to wait a second or two for KeymanWeb to load. Then click in the text field you wish to type into. The KeymanWeb user interface will appear and you can type in your language.</li>
</ol>
