<h2>Installing Keyman for Mac OS X</h2>

<p>
	<i>Keyman for Mac OS X</i> is provided as a standard Mac disk image (.dmg) file.  It should be installed according to the following instructions.
</p>
<ul>
	<li>
		Double click the DMG file and drag and drop the Keyman app into the ‘Input Methods’ folder. 
	</li>
	<li>
		Go to "System Preferences", then "Keyboard" and select the "Input Sources" tab. 
		Or, click on the input menu (usually a country flag or language icon) that appears on the right hand side of the menu bar, 
		then select "Open Keyboard Preferences". (This step may differ slightly depending on the OS version.)
	</li>
	<li>
		Click on "+", then find Keyman under Multi-Language and double click that entry to add as an input method.
	</li>
	<li>
		Click the input menu and switch to Keyman. Click the input menu again to see the installed keyboards in Keyman:Keyboards menu. 
		You may also switch to another keyboard from this menu. 
	</li>
	<li>
		Now you can start typing with the selected keyboard in any text editor or application that uses standard OS X input methods. 
	</li>
</ul>
<h2>Uninstalling</h2>
<p>
	Please follow the following steps to uninstall <i>Keyman for Mac OS X</i>.
</p>
<ul>
	<li>
		Select a different input source.
	</li>
	<li>
		Open the input sources, selecting Keyman, and press the “-“ button.
	</li>
	<li>
		Open the Input Methods folder and move Keyman to Trash.
	</li>
</ul>
